print("测试一")
str = "y0n"

# 判断所有字符都是数字或者字母
print(str.isalnum())
# 判断所有字符都是字母
print(str.isalpha())
# 判断所有字符都是数字
print(str.isdigit())
# 判断所有字符都是小写
print(str.islower())
# 判断所有字符都是大写
print(str.isupper())
# 判断所有单词都是首字母大写，像标题
print(str.istitle())
# 判断所有字符都是空白字符、\t、\n、\r
print(str.isspace())

